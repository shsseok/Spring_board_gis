package geomex.notice.model;

public class CctvVo {

	private String mgrNo; // 관리번호
	private String cctvNm; // CCTV명
	private String cctvDesc; // CCTV관리명

	public String getMgrNo() {
		return mgrNo;
	}

	public void setMgrNo(String mgrNo) {
		this.mgrNo = mgrNo;
	}

	public String getCctvNm() {
		return cctvNm;
	}

	public void setCctvNm(String cctvNm) {
		this.cctvNm = cctvNm;
	}

	public String getCctvDesc() {
		return cctvDesc;
	}

	public void setCctvDesc(String cctvDesc) {
		this.cctvDesc = cctvDesc;
	}

}
